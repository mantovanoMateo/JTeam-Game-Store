    private void jLabelIngresarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelIngresarMouseClicked
        // TODO add your handling code here: este llama a las verificaciones, si es correcto ingresa, sino printea que la contrasenia es incorrecta
        user=steam.buscarClienteBasicoPorNickname(ingresoUser.getText());
        if(user!=null){
            if(!user.getContrasena().equals(jPasswordField1.getText())){
                JOptionPane.showMessageDialog(this,"Contrase単a incorrecta");
            }else if(user.getActivo()==false){
                JOptionPane.showMessageDialog(this,"Usuario dado de baja, contactese con un admin");
            }else{
                MenuPrincipalUserBasico menuBasico = new MenuPrincipalUserBasico(this, this.steam, (UserBasico) user,this.file,this.mapper);
                menuBasico.setVisible(true);
            }
        }else if (user == null){
            user=this.steam.buscarClientePremiumPorNickname(ingresoUser.getText());
            if(user!=null){
                if(!user.getContrasena().equals(jPasswordField1.getText())){
                    JOptionPane.showMessageDialog(this,"Contrase単a incorrecta");
                }else if (user.getActivo()==false){
                    JOptionPane.showMessageDialog(this,"Usuario dado de baja, contactese con un admin");
                }else {
                    MenuPrincipalUserPremium menuPremium = new MenuPrincipalUserPremium(this, this.steam, (UsuarioPremium) user,this.file,this.mapper);
                    menuPremium.setVisible(true);
                }
            }
        }

        if(user==null){
            user=steam.buscarAdminPorNombre(ingresoUser.getText());
        }
        
        if(user == null){
            JOptionPane.showMessageDialog(null,"Usuario no encontrado");
        }else if(user instanceof Admin){
            if(!user.getContrasena().equals(jPasswordField1.getText())){
                JOptionPane.showMessageDialog(this,"Contrase単a incorrecta");
            }else if (user.getActivo()==false){
                JOptionPane.showMessageDialog(this,"Usuario dado de baja, contactese con un admin");
            }else {
                MenuPrincipalAdmin menuAdmin = new MenuPrincipalAdmin(this, this.steam, (Admin) user,this.file,this.mapper);
                menuAdmin.setVisible(true);
            }
        }

        if(user!=null){
            if(user.getActivo()==true){
                dispose();
            }
        }


        
    }//GEN-LAST:event_jLabelIngresarMouseClicked

