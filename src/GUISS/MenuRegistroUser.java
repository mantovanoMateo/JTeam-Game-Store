    private void botonRegsitrarseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonRegsitrarseMouseClicked
        // TODO add your handling code here:
        Boolean dniOK=false;
        Boolean mailOK=false;
        Boolean contraOK=false;
        Boolean nickNameOK=false;
        
        UserBasico nuevo= new UserBasico();
        nuevo.setNombre(campoNombre.getText());
        nuevo.setApellido(campoApellido.getText());
        if(Validar.dni(campoDni.getText(), this.steam.getUsersBasicos(),this.steam.getUsuariosPremium())){
            dniOK=true;
            nuevo.setDni(campoDni.getText());
        }else{
            JOptionPane.showMessageDialog(this, "User ya registrado");
        }
        
        ArrayList<Boolean> valiEmail=Validar.mail(campoEmail.getText(), this.steam.getUsuariosPremium(),this.steam.getUsersBasicos());
        if(valiEmail.get(0)==true){
            nuevo.setMail(campoEmail.getText());
            mailOK=true;
        }else if(valiEmail.get(1)==false){
            JOptionPane.showMessageDialog(this, "El mail no continene @ o .com");
        }else if(valiEmail.get(2)==false){
            JOptionPane.showMessageDialog(this, "El mail ya se encuentra registrado en el sistema");
        }
        
        ArrayList<Boolean> validacionContra = Validar.contrasena(campoContrasenia.getText());
            if(validacionContra.get(0)==true){
                nuevo.setContrasena(campoContrasenia.getText());
                contraOK=true;
            }else if(validacionContra.get(1)==false){
                JOptionPane.showMessageDialog(this, "Debe contener almenos una mayuscula");
            }else if(validacionContra.get(2)==false){
                JOptionPane.showMessageDialog(this, "Debe contener almenos un numero");
            }else if(validacionContra.get(3)==false){
                JOptionPane.showMessageDialog(this, "Debe contener almenos ocho caracteres");
            }


        if(Validar.nickname(campoNickName.getText(), this.steam.getUsersBasicos(),this.steam.getUsuariosPremium())){
            nuevo.setNickName(campoNickName.getText());
            nickNameOK=true;
        }
        
        if(dniOK && mailOK && contraOK && nickNameOK){
            this.steam.agregarUsuario(nuevo);
            try {
                this.mapper.writerWithDefaultPrettyPrinter().writeValue(this.file,this.steam);
            } catch (IOException e) {
                throw new RuntimeException(e);
            }
            lw.setVisible(true);
            dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Ingreso algun dato incorrecto");
        }
        
    }//GEN-LAST:event_botonRegsitrarseMouseClicked

