    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row=jTable1.getSelectedRow();
        DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
        this.reactivar=this.steam.buscarClienteBasicoPorNickname(model.getValueAt(row, 1).toString());
        if(this.reactivar==null){
            this.reactivar=this.steam.buscarClienteBasicoPorNickname(model.getValueAt(row, 1).toString());
        }
    }//GEN-LAST:event_jTable1MouseClicked


    private void listarInactivos(ArrayList<Cliente> clientes){
        DefaultTableModel datos=(DefaultTableModel)jTable1.getModel();
        datos.setNumRows(0);
        String tipo="";
        for(Cliente cliente: clientes){
                if(cliente instanceof UserBasico){
                    tipo="Basico";
                }else{
                    tipo="Premium";
                }
                Object [] fila = {cliente.getNombre(),cliente.getNickName(),cliente.getMail(),cliente.getDni(),tipo};
                datos.addRow(fila);  
        }
    }
    
