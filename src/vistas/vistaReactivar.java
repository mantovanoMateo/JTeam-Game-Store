    private void botonReactivarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonReactivarMouseClicked
        // TODO add your handling code here:
        if(reactivar != null){
            reactivar.setActivo(true);
            if(reactivar instanceof UserBasico){
                steam.agregarUsuario((UserBasico) reactivar);
            }else{
                steam.agregarUsuarioPremimu((UsuarioPremium) reactivar);
            }
            JOptionPane.showMessageDialog(this,"Cliente reactivado");
            listarInactivos(steam.listarClientesDadosDeBaja());
        }else{
            JOptionPane.showMessageDialog(this,"Primero seleccione un cliente");
        }
    }//GEN-LAST:event_botonReactivarMouseClicked

