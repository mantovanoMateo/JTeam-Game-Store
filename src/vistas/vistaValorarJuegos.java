    private void textoValorarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_textoValorarMouseClicked
        if(aValorar!=null){
            Double valoracion=Double.parseDouble(campoValoracion.getText());
            aValorar.setCantValoraciones(aValorar.getCantValoraciones()+1);
            Double nuevaVal=(Double) ((aValorar.getValoracion()+valoracion)/aValorar.getCantValoraciones());
            aValorar.setValoracion(nuevaVal);
            this.steam.reemplazaJuego(aValorar);
            this.usuario.getValorados().add(aValorar.getIdJuego());
            listarNoValorados(usuario.getComprados());
            JOptionPane.showMessageDialog(this, "Valorado correctamente con un " + valoracion);

        }else{
            JOptionPane.showMessageDialog(this, "Debe seleccionar un juego");
        }
    }//GEN-LAST:event_textoValorarMouseClicked

