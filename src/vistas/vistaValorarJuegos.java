    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row=jTable1.getSelectedRow();
        DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
        this.aValorar=this.steam.retornaJuego(model.getValueAt(row, 0).toString());
    }//GEN-LAST:event_jTable1MouseClicked
    
    private void listarNoValorados(ArrayList<Juego> juegos){
        DefaultTableModel datos=(DefaultTableModel)jTable1.getModel();
        datos.setNumRows(0);
        for(Juego juego: juegos){
                if(!this.usuario.getValorados().contains(juego.getIdJuego())){
                    Object [] fila = {juego.getNombre(),juego.getCategoria()};
                    datos.addRow(fila);
                }
        }
    }

